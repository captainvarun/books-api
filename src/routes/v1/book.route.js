const express = require('express');
const { bookController } = require('../../controllers/index.js');
const validate = require('../../middlewares/validate.js');
const { bookValidation } = require('../../utils/validations/index.js');
const auth = require('../../middlewares/auth.js');

const router = express.Router();

router.get('/', bookController.getBooks);

router.get('/:bookId', validate(bookValidation.getBookById), bookController.getBook);

router.post('/', auth('admin'), validate(bookValidation.createBook), bookController.createBook);

router.put('/:bookId', auth('admin'), validate(bookValidation.updateBook), bookController.updateBook);

router.delete('/:bookId', auth('admin'), validate(bookValidation.deleteBook), bookController.deleteBook);

module.exports = router;

